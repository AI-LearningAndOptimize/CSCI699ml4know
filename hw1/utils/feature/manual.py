"""
Hand-crafted features. Code adapted from https://sklearn-crfsuite.readthedocs.io/en/latest/tutorial.html.
It returns a list of list of dictionaries.
"""

from .common import sent2labels


def word2features(sent, i):
    word = sent[i][0]
    postag = sent[i][1]

    features = {
        'bias': 1.0,
        'word.lower()': word.lower(),
        'word[-3:]': word[-3:],
        'word[-2:]': word[-2:],
        'word.isupper()': word.isupper(),
        'word.istitle()': word.istitle(),
        'word.isdigit()': word.isdigit(),
        'postag': postag,
        'postag[:2]': postag[:2],
    }
    if i > 0:
        word1 = sent[i - 1][0]
        postag1 = sent[i - 1][1]
        features.update({
            '-1:word.lower()': word1.lower(),
            '-1:word.istitle()': word1.istitle(),
            '-1:word.isupper()': word1.isupper(),
            '-1:postag': postag1,
            '-1:postag[:2]': postag1[:2],
        })
    else:
        features['BOS'] = True

    if i < len(sent) - 1:
        word1 = sent[i + 1][0]
        postag1 = sent[i + 1][1]
        features.update({
            '+1:word.lower()': word1.lower(),
            '+1:word.istitle()': word1.istitle(),
            '+1:word.isupper()': word1.isupper(),
            '+1:postag': postag1,
            '+1:postag[:2]': postag1[:2],
        })
    else:
        features['EOS'] = True

    return features


def sent2features(sent):
    return [word2features(sent, i) for i in range(len(sent))]


def manual_feature_extractor(sentences):
    X = [sent2features(s) for s in sentences]
    word_tuple_length = sentences[0][0]
    if len(word_tuple_length) == 3:
        y = [sent2labels(s) for s in sentences]
    elif len(word_tuple_length) == 2:
        y = None
    else:
        raise ValueError('Each word in sent must be (token, postag, label) or (token postag), but got length {}'.format(
            word_tuple_length))
    return X, y
